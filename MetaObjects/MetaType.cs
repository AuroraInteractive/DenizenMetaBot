using System;
using System.Collections.Generic;
using System.Text;

namespace DenizenBot.MetaObjects
{
    /// <summary>
    /// Represents a type of meta documentations.
    /// </summary>
    public class MetaType
    {
        /// <summary>
        /// The name of the object meta type.
        /// </summary>
        public string Name;

        /// <summary>
        /// The webpath for this object meta type (eg "cmds").
        /// </summary>
        public string WebPath;
    }
}
